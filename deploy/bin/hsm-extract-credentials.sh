#!/bin/bash
# hsm-extract-credentials.sh — Extract credentials from SoftHSM2 into OpenClaw .env
# Called by systemd ExecStartPre to inject secrets at runtime.
# The .env file is written to ~/.openclaw/.env which OpenClaw reads natively.
# File is mode 600, owned by lobsec — never world-readable.
# Wiped on service stop (ExecStopPost).
#
# Also patches openclaw.json to inject CF-Access headers from HSM,
# since OpenClaw does not support env var interpolation in headers.

set -euo pipefail

SOFTHSM_LIB=/usr/lib/softhsm/libsofthsm2.so
export SOFTHSM2_CONF="${SOFTHSM2_CONF:-/opt/lobsec/boot/softhsm2.conf}"
TOKEN_LABEL=lobsec

# OpenClaw reads .env from its config directory
ENV_FILE=/opt/lobsec/.openclaw/.env
CONFIG_FILE=/opt/lobsec/.openclaw/openclaw.json
HSM_LOG=/opt/lobsec/logs/hsm-access.log

# Read HSM PIN from environment (never from disk)
HSM_PIN="${LOBSEC_HSM_PIN:-}"
if [ -z "$HSM_PIN" ]; then
    echo "ERROR: LOBSEC_HSM_PIN not set" >&2
    exit 1
fi

# Structured HSM access logging
hsm_log() {
    local level="$1" event="$2" label="${3:-}" detail="${4:-}"
    local ts
    ts=$(date -Iseconds)
    printf '{"ts":"%s","level":"%s","event":"%s","label":"%s","detail":"%s"}\n' \
        "$ts" "$level" "$event" "$label" "$detail" >> "$HSM_LOG"
}

mkdir -p /opt/lobsec/logs
hsm_log "INFO" "extraction_start" "" "pid=$$"

extract_data_object() {
    local label="$1"
    local value
    if value=$(pkcs11-tool --module "$SOFTHSM_LIB" \
        --token-label "$TOKEN_LABEL" \
        --login --pin "$HSM_PIN" \
        --read-object --type data --label "$label" 2>/dev/null); then
        hsm_log "INFO" "extract_ok" "$label" "bytes=${#value}"
        printf '%s' "$value"
    else
        hsm_log "ERROR" "extract_fail" "$label" "pkcs11-tool exit=$?"
        return 1
    fi
}

# Extract all credentials from HSM
TELEGRAM_BOT_TOKEN=$(extract_data_object "telegram-bot-token")
OLLAMA_API_KEY=$(extract_data_object "ollama-api-key")
GATEWAY_AUTH_TOKEN=$(extract_data_object "gateway-auth-token")
ANTHROPIC_API_KEY=$(extract_data_object "anthropic-api-key")
JETSON_CF_CLIENT_ID=$(extract_data_object "jetson-cf-client-id")
JETSON_CF_CLIENT_SECRET=$(extract_data_object "jetson-cf-client-secret")
PERPLEXITY_API_KEY=$(extract_data_object "perplexity-api-key")
TOMORROW_IO_API_KEY=$(extract_data_object "tomorrow-io-api-key")
GMAIL_APP_PASSWORD=$(extract_data_object "gmail-app-password")
RADICALE_PASSWORD=$(extract_data_object "radicale-password")

# Write gateway .env — NO cloud API keys (proxy holds those)
umask 077
cat > "$ENV_FILE" <<ENVEOF
# Auto-generated by lobsec HSM extraction — DO NOT EDIT
# Cloud API keys are NOT here — they live in the proxy only.
# This file is regenerated on every start — $(date -Iseconds)

# Telegram channel
TELEGRAM_BOT_TOKEN="${TELEGRAM_BOT_TOKEN}"

# Gateway auth (also used as proxy token for LLM calls)
OPENCLAW_GATEWAY_TOKEN="${GATEWAY_AUTH_TOKEN}"

# Perplexity web search (Sonar)
PERPLEXITY_API_KEY="${PERPLEXITY_API_KEY}"

# Tomorrow.io weather
TOMORROW_IO_API_KEY="${TOMORROW_IO_API_KEY}"

# Gmail SMTP/IMAP (app password)
GMAIL_USER=lobsec.node.bc11@gmail.com
GMAIL_APP_PASSWORD="${GMAIL_APP_PASSWORD}"

# Radicale CalDAV/CardDAV
RADICALE_USER=lobsec
RADICALE_PASSWORD="${RADICALE_PASSWORD}"
RADICALE_URL=http://127.0.0.1:5232
ENVEOF

chmod 600 "$ENV_FILE"

# Write proxy .env — has real cloud API keys + proxy token
PROXY_ENV_FILE=/opt/lobsec/.openclaw/.env.proxy
cat > "$PROXY_ENV_FILE" <<ENVEOF
# Auto-generated by lobsec HSM extraction — DO NOT EDIT
# Cloud API keys for the lobsec proxy credential injector.
# This file is regenerated on every start — $(date -Iseconds)

# Proxy auth token (matches gateway's OPENCLAW_GATEWAY_TOKEN)
OPENCLAW_GATEWAY_TOKEN="${GATEWAY_AUTH_TOKEN}"

# Cloud provider API keys (proxy injects these into outbound requests)
ANTHROPIC_API_KEY="${ANTHROPIC_API_KEY}"

# Sovereign backend keys (proxy passes through for egress auditing)
OLLAMA_API_KEY="${OLLAMA_API_KEY}"

# Jetson CF-Access credentials (proxy injects into Jetson requests)
JETSON_CF_CLIENT_ID="${JETSON_CF_CLIENT_ID}"
JETSON_CF_CLIENT_SECRET="${JETSON_CF_CLIENT_SECRET}"
ENVEOF

chmod 600 "$PROXY_ENV_FILE"

# CF-Access headers for Jetson are now injected by the proxy (via .env.proxy),
# not patched into openclaw.json. The proxy adds them on forward.

# Generate Radicale htpasswd from HSM password (regenerated each startup)
if [ -n "$RADICALE_PASSWORD" ]; then
    HTPASSWD_FILE=/opt/lobsec/config/radicale-users
    RAD_HASH=$(python3 -c "import sys,bcrypt; print(bcrypt.hashpw(sys.argv[1].encode(),bcrypt.gensalt()).decode())" "$RADICALE_PASSWORD" 2>/dev/null)
    if [ -n "$RAD_HASH" ]; then
        echo "lobsec:${RAD_HASH}" > "$HTPASSWD_FILE"
        chmod 600 "$HTPASSWD_FILE"
        hsm_log "INFO" "htpasswd_generated" "radicale" "file=$HTPASSWD_FILE"
    else
        hsm_log "WARN" "htpasswd_skip" "radicale" "bcrypt not available"
    fi
fi

hsm_log "INFO" "extraction_complete" "" "env=$ENV_FILE,config_patched=true"
echo "Credentials extracted from HSM to $ENV_FILE ($(date -Iseconds))"
